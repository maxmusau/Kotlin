fun main(args: Array<String>) {
    //data types
    // numeric data types
    // Byte
    var myByte: Byte =127 //storage between -128 to 128
    println(myByte)
    var myShort: Short=30000 //-32k to 32k
    println(myShort)
    var myInt: Int=2000000000 //-2b to 2b
    println(myInt)
    var myLong: Long=9000000000000000000 //-9t to 9t
    println(myLong)

    // Double
    var height:Double=2.12
    println(height)

    //Float
    var bmi:Float=18.5f
    println(bmi)

    // String
    val course:String="Blockchain" //double quotation marks
    println(course)

    // character Char
    var letter:Char ='A' //single quotation marks 
    println(letter) 

}