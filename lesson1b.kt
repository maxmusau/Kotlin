fun main(args: Array<String>) {
    // kotlin variables
    //var and val keywords are used to define kotlin variables
    var name="Modcom"
    println(name)
    // var variables can be reassigned
    name="Kenya"
    println(name) 

    //  val variables cannot be reassined
    val month="October"
    println(month)

    // month="November" -it will give you an error
    val num=34
    println(num)
}